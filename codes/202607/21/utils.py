def load_sentiment_dictionary():
    """
    감성 분석에 사용될 긍정 및 부정 단어 사전을 로드합니다.
    실제 애플리케이션에서는 외부 파일이나 데이터베이스에서 로드합니다.
    """
    positive_words = {
        "좋다", "최고", "훌륭하다", "만족", "기쁘다", "행복", "사랑", "감사", "멋지다", "성공",
        "잘한다", "아름답다", "완벽", "대박", "추천", "기대", "신기하다", "칭찬", "재미있다", "웃기다"
    }
    negative_words = {
        "나쁘다", "최악", "끔찍하다", "불만", "슬프다", "고통", "증오", "실망", "못생기다", "실패",
        "망하다", "지루하다", "어렵다", "문제", "짜증", "화나다", "걱정", "불안", "힘들다", "귀찮다"
    }
    return positive_words, negative_words
